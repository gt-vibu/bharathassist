/**
 * Client-side static translation helper
 */
export function getStaticTranslation(text: string, targetLanguage: string): string {
  if (!targetLanguage || targetLanguage === 'English') return text;
  
  const langDict = UI_TRANSLATIONS[targetLanguage];
  if (!langDict) return text;

  // Trim and try exact match
  const trimmed = text.trim();
  if (langDict[trimmed]) return langDict[trimmed];

  // Check case-insensitive match
  const lower = trimmed.toLowerCase();
  for (const [eng, trans] of Object.entries(langDict)) {
    if (eng.toLowerCase() === lower) {
      return trans;
    }
  }

  return text;
}
